#include "libft.h"

int	ft_atoi(const char *str)
{
	int					sign;
	unsigned long long	result;
	unsigned long long	max;

	result = 0;
	sign = 1;
	max = LONG_MAX;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
		sign *= -1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		if (result > max && sign == 1)
			return (-1);
		if (result > max && sign == -1)
			return (0);
		str++;
	}
	return (result * sign);
}
